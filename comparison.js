var x = 5;

console.log(x == 8);      //equal to
console.log(x == 5);
console.log(x == "5");

console.log(x === 5);     //equal value
console.log(x === "5");   //equal type

console.log(x != 8);      //not equal

console.log(x !==5);      //not equal value
console.log(x !== "5");   //not equal type
console.log(x !== 8);

console.log(x > 8);       //greater than
console.log(x < 8);       //less than
console.log(x >= 8);      //greater than equal
console.log(x <= 8);      //less than equal
